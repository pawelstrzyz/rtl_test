   <td class="popupText"><p><b>Co znaczą skróty HTTA, HTDA, HTKA i HTCA - jak je używać ?</b><br><br>
    Edytor Meta Tagów pozwala na zdefiniowanie domyslnych wpisów meta dla całego serwisu.
    Można również zdefiniować indywidualne wartości dla wybranych stron.
    <pre>
    HT = Tagi sekcji HEAD
    T  = Tytuł 
    A  = Wszystko
    D  = Opis
    K  = Słowa kluczowe
    C  = Kategorie *
    P  = Kategorie i Produkty **
</pre>  
    <b>* UWAGA:</b> Opcja HTCA działa tylko na stronie index. Powoduje ona wyświetlenie nazwy kategorii w tytule.<br><br>

<b>** UWAGA:</b> Opcja HTPA działa tylko na stronie informacyjnej produktu. Jeżeli jest zaznaczona, Nazwa kategorii w której znajduje się produkt zostanie dodana do tytułu i znanczników meta .
<br><br>

    Jeżeli jest zaznaczona opcja HTTA, wówczas w znaczniku Title w sekcji HEAD wyświetlany jest domyślny tytuł zdefiniowany dla całego seriwsu oraz tytuł zdefiniowany dla wybranej strony.<br><br>
    Np. Jeżeli została zaznaczona opcja HTTA i tytuł dla wybranej strony jest zdefiniowany jako "Moja Strona", a domyślny tytuł całego serwisu jest "eSklep-Os", wówczas na wybranej stronie tytuł będzie następujący :<br>
    <pre>
    Jeżeli włączono opcję HTTA - tytuł strony jest
    Moja Strona eSklep-Os.com
    Jeżeli wyłączono opcję HTTA - tytuł strony jest
    Moja Strona
    </pre>
    Analogiczna sytuacja występuje dla pozostałych opcji dotyczących znaczników Opis i Słowa kluczowe.
    </p>
